export function languageSuggestions(): string[] {
  const displayNames = new Intl.DisplayNames(["en"], { type: "language" });
  return ISO_639_CODES.map((code) => displayNames.of(code) ?? code).sort(
    (a, b) => a.localeCompare(b)
  );
}
